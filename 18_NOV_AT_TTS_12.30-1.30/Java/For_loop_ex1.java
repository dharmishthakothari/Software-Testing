public class For_loop_ex1 {

    public static void main(String[] args) {
        for (int i = 10; i <= 20; i++) {
            // System.out.println("Hello world " + i);
            System.out.println(i);
        }

        System.out.println("Reverse ");
        for (int i = 20; i >= 10; i--) {
            System.out.println(i);
        }
    }
}
